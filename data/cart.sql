SET SQL_MODE="NO_AUTO_VALUE_ON_ZERO";

CREATE TABLE IF NOT EXISTS cart (
    id INT(45) NOT NULL AUTO_INCREMENT,
    akunId INT(45) NOT NULL,
    produkId INT(45) NOT NULL,
    quantity INT(11) NOT NULL DEFAULT 1,
    alamat TEXT NOT NULL,
    createdAt DATETIME DEFAULT CURRENT_TIMESTAMP,
    PRIMARY KEY (id),
    CONSTRAINT fk_cart_akun FOREIGN KEY (akunId) REFERENCES akun(id) ON DELETE CASCADE,
    CONSTRAINT fk_cart_produk FOREIGN KEY (produkId) REFERENCES produk(id) ON DELETE CASCADE
);